#This code calculates the speed in kmh/mph
# To enter the kilometers per hour as input
kmh = int(input("Enter km/h: "))
# Speed in miles can be converted by multiplying with 0.6214
mph =  0.6214 * kmh
# The speed is calculated and printed accordingly
print("Speed:", kmh, "KM/H = ", mph, "MPH")

